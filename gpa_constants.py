# gpa_constants.py
"""
Модуль констант для моделирования ГПА в режиме "Магистраль".
Значения соответствуют типовым расчётным практикам и не должны изменяться
в ходе единичного моделирования.
"""

# === Базовые термодинамические условия ===
REF_TEMPERATURE_K: float = 288.15       # Нормальная температура, K
REF_PRESSURE_MPA: float = 0.101325      # Нормальное давление, МПа
