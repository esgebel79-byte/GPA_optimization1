POLYTROPIC_EXPONENT_RATIO: float = 0.20 # Показатель политропы для природного газа (m ≈ (k-1)/k, где k ≈ 1.25...1.30)

# === Свойства газа и топлива (средние эксплуатационные) ===
COMPRESSIBILITY_FACTOR: float = 0.85    # Средний Z для природного газа (5–8 МПа, 10–30 °C)
FUEL_HEAT_VALUE_MJ_NM3: float = 35.0    # Низшая теплота сгорания, МДж/нм³
FUEL_DENSITY_RATIO: float = 0.60        # Относительная плотность топливного газа по воздуху
AIR_DENSITY_KG_M3: float = 1.2044       # Плотность сухого воздуха при н.у.
GAS_MOLAR_MASS_KG_KMOL: float = 17.0    # Молярная масса кг/кмоль

# === Механические и эксплуатационные коэффициенты ===
MECHANICAL_EFFICIENCY: float = 0.95     # Механический КПД привода ЦБН
STANDARD_HEAT_REF_KJ_NM3: float = 32149.44 # Условная теплота сгорания (7700 ккал/нм³)
GTU_FUEL_FLOW_NOMINAL_KG_H: float = 2355.7 # Номинальный расход топливного газа при номинальной мощности. Значение 2355.7 кг/ч соответствует удельному расходу ~139 кг/(МВт·ч) при КПД ~26%.
GTU_NOMINAL_POWER_MW: float = 16.9 # Номинальная мощность ГТУ. 
